import numpy as np
arr=np.array([1,2,3,4,5,6])
print(np.sum(arr)) 
print(np.mean(arr))  
print(np.std(arr))  # it will give the standard deviation of all the elements in the array
print(np.var(arr))  # it will give the variance of all the elements in the array
print(np.min(arr))  # it will give the minimum value from the array
print(np.max(arr))  # it will give the maximum value from the array